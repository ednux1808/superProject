    public void abrirDoc() {
       clearEditor();
        if (seleccionar.showDialog(null, "Abrir") == JFileChooser.APPROVE_OPTION) {
            archivo = seleccionar.getSelectedFile();
            if (archivo.canRead()) {
                String documento = this.app.abrirArchivo(archivo);
                this.textEditor.setText(documento);
            }
        }

    }

    public void guardarDoc() {

        if (seleccionar.showDialog(null, "Guardar") == JFileChooser.APPROVE_OPTION) {
            archivo = seleccionar.getSelectedFile();
            String documento = this.textEditor.getText();
            this.app.guardarArchivo(archivo, documento);
        }
    }

